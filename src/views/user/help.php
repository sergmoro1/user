<h4>Профиль</h4>
<p>Можно изменить имя и email. Можно добавить аватар. Аватар используется при выводе комментариев.
</p>
<p>
  Одновременно может быть несколько аватаров. Используется только главный (самый "верхний").
</p>
<p>
  Чтобы сделать аватар главным, достаточно нажать мышкой на нужном изображении и перетащить его вверх. 
</p>
